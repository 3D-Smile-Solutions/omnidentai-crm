// POST /auth/login
//  UPDATED: POST /auth/login - Return external_id
export const login = async (req, res) => {
  const { email, password } = req.body;

  try {
    const { data, error } = await supabase.auth.signInWithPassword({ email, password });
    if (error) return res.status(400).json({ error: error.message });

    // Fetch profile with external_id
    const { data: profile, error: profileError } = await supabase
      .from("client_profiles")
      .select("id, first_name, last_name, external_id") // â† ADD external_id
      .eq("id", data.user.id)
      .single();

    setRefreshCookie(res, data.session.refresh_token);

    // Create session log
    const sessionId = await getOrCreateSession(data.user.id, req);

    res.json({
      message: "Login successful",
      session: { access_token: data.session.access_token },
      user: {
        ...data.user,
        first_name: profile?.first_name,
        last_name: profile?.last_name,
        external_id: profile?.external_id, // â† ADD THIS
        sessionId: sessionId
      },
      sessionLogId: sessionId
    });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};


// âœ… UPDATED: GET /auth/me - Return external_id
export const me = async (req, res) => {
  const token = req.headers.authorization?.split(" ")[1];

  try {
    if (token) {
      const { data, error } = await supabase.auth.getUser(token);
      if (!error && data?.user) {
        // Fetch profile data with external_id
        const { data: profile, error: profileError } = await supabase
          .from("client_profiles")
          .select("id, first_name, last_name, external_id") // â† ADD external_id
          .eq("id", data.user.id)
          .single();

        if (!profileError && profile) {
          return res.json({ 
            user: { 
              ...data.user, 
              first_name: profile.first_name, 
              last_name: profile.last_name,
              external_id: profile.external_id // â† ADD THIS
            } 
          });
        }
        return res.json({ user: data.user });
      }
    }

    const refreshToken = req.cookies?.refresh_token;
    if (!refreshToken) return res.status(401).json({ error: "Not authenticated" });

    const { data, error } = await supabase.auth.refreshSession({ refresh_token: refreshToken });
    if (error) return res.status(401).json({ error: error.message });

    setRefreshCookie(res, data.session.refresh_token);

    // Fetch profile data with external_id
    const { data: profile, error: profileError } = await supabase
      .from("client_profiles")
      .select("id, first_name, last_name, external_id") // â† ADD external_id
      .eq("id", data.user.id)
      .single();

    res.json({ 
      user: { 
        ...data.user, 
        first_name: profile?.first_name, 
        last_name: profile?.last_name,
        external_id: profile?.external_id // â† ADD THIS
      }, 
      access_token: data.session.access_token 
    });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};


// POST /auth/logout
export const logout = async (req, res) => {
  try {
    const userId = req.user?.id;
    
    if (userId) {
      await endSession(userId);
    }

    await supabase.auth.signOut();
    res.clearCookie("refresh_token", {
      path: "/",
      sameSite: process.env.NODE_ENV === "production" ? "None" : "Lax",
      secure: process.env.NODE_ENV === "production",
    });
    res.json({ message: "Logged out" });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};


// POST /auth/refresh
export const refresh = async (req, res) => {
  try {
    const refreshToken = req.cookies?.refresh_token;
    if (!refreshToken) return res.status(401).json({ error: "Missing refresh token" });

    const { data, error } = await supabase.auth.refreshSession({ refresh_token: refreshToken });
    if (error) return res.status(401).json({ error: error.message });

    setRefreshCookie(res, data.session.refresh_token);

    res.json({ access_token: data.session.access_token, user: data.user });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
};

